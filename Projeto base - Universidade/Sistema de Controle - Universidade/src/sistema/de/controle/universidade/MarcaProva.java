    private void marcarProva()
    {
        ResultSet rs = null;
        ConectarAOBanco cd = new ConectarAOBanco();
        try{
            String disciplina = jTextField1.getText().toString();
            String data = jTextField2.getText().toString();
            String professor = jTextField3.getText().toString();
            String materia = jTextArea1.getText().toString();
            cd.ConectarBanco();
            if(disciplina.equals(""))
            {
                JOptionPane.showMessageDialog( this, "Campo disciplina em branco. Preenchimento obrigatório!" );
            }
            else{
                String query = "INSERT INTO universidade.provas(id_aluno,disciplina,data,professor,materia) VALUES('"+ControleUniversidade.a+"','"+disciplina+"','"+data+"','"+professor+"','"+materia+"')";        
                int r;
                r = cd.updatequery(query);
                if(r == 1)
                {
                     JOptionPane.showMessageDialog( this, "Data de prova inserida com sucesso!" );
                }
                else {
                     JOptionPane.showMessageDialog( this, "Data de prova não foi inserida!" );
                }
            }
        }
        catch( Exception sqlex ){
            JOptionPane.showMessageDialog( this, "Não é possível adicionar nova data de prova" );
        }
    }
